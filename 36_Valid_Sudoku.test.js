import { describe, test, expect } from 'bun:test'
import { isValidSudoku } from './36_Valid_Sudoku'

describe('36_Valid_Sudoku', () => {
    test('case 1', () => {
        let board = [
            ['5', '3', '.', '.', '7', '.', '.', '.', '.'],
            ['6', '.', '.', '1', '9', '5', '.', '.', '.'],
            ['.', '9', '8', '.', '.', '.', '.', '6', '.'],
            ['8', '.', '.', '.', '6', '.', '.', '.', '3'],
            ['4', '.', '.', '8', '.', '3', '.', '.', '1'],
            ['7', '.', '.', '.', '2', '.', '.', '.', '6'],
            ['.', '6', '.', '.', '.', '.', '2', '8', '.'],
            ['.', '.', '.', '4', '1', '9', '.', '.', '5'],
            ['.', '.', '.', '.', '8', '.', '.', '7', '9'],
        ]

        expect(isValidSudoku(board)).toStrictEqual(true)
    })
    test('case row', () => {
        let board = [
            ['8', '3', '.', '3', '.', '.', '.', '.', '.'],
            ['6', '.', '.', '.', '.', '.', '.', '.', '.'],
            ['.', '9', '.', '.', '.', '.', '.', '.', '.'],
            ['.', '.', '.', '.', '6', '.', '.', '.', '.'],
            ['4', '.', '.', '8', '.', '.', '.', '.', '.'],
            ['7', '.', '.', '.', '2', '.', '.', '.', '.'],
            ['.', '6', '.', '.', '.', '.', '.', '.', '.'],
            ['.', '.', '.', '4', '1', '.', '.', '.', '.'],
            ['.', '.', '.', '.', '8', '.', '.', '.', '.'],
        ]

        expect(isValidSudoku(board)).toStrictEqual(false)
    })
    test('case column', () => {
        let board = [
            ['8', '3', '.', '.', '7', '.', '.', '.', '.'],
            ['6', '.', '.', '1', '9', '5', '.', '.', '.'],
            ['.', '9', '.', '.', '.', '.', '.', '6', '.'],
            ['8', '.', '.', '.', '6', '.', '.', '.', '3'],
            ['4', '.', '.', '8', '.', '3', '.', '.', '1'],
            ['7', '.', '.', '.', '2', '.', '.', '.', '6'],
            ['.', '6', '.', '.', '.', '.', '2', '8', '.'],
            ['.', '.', '.', '4', '1', '9', '.', '.', '5'],
            ['.', '.', '.', '.', '8', '.', '.', '7', '9'],
        ]

        expect(isValidSudoku(board)).toStrictEqual(false)
    })
    test('case box', () => {
        let board = [
            ['8', '3', '.', '.', '7', '.', '.', '.', '.'],
            ['6', '.', '.', '1', '9', '5', '.', '.', '.'],
            ['.', '9', '8', '.', '.', '.', '.', '6', '.'],
            ['.', '.', '.', '.', '6', '.', '.', '.', '3'],
            ['4', '.', '.', '8', '.', '3', '.', '.', '1'],
            ['7', '.', '.', '.', '2', '.', '.', '.', '6'],
            ['.', '6', '.', '.', '.', '.', '2', '8', '.'],
            ['.', '.', '.', '4', '1', '9', '.', '.', '5'],
            ['.', '.', '.', '.', '8', '.', '.', '7', '9'],
        ]

        expect(isValidSudoku(board)).toStrictEqual(false)
    })
})
